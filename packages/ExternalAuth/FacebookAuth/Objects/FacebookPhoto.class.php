<?php
/**
 * Facebook's Photo class
 * 
 * @author Aram Gevorgyan
 */
class FacebookPhoto {
	
	/**
	 * Id of current photo
	 * @var integer
	 */	
	public $id = null;
	
	/**
	 * Current photo small picture source
	 * @var string
	 */
	public $picture = null;
	
	/**
	 * Current photo real source
	 * @var string
	 */
	public $source = null;
	
	/**
	 * current photo height in pixels
	 * @var integer
	 */
	public $height = null;
	
	/**
	 * current photo width in pixels
	 * @var integer
	 */
	public $width = null;
		
	}
